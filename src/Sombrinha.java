public class Sombrinha {
    private int id;
    private int maxUsers;
    private char praia;

    public Sombrinha(int id, int maxUsers, char praia) {
        this.id = id;
        this.maxUsers = maxUsers;
        this.praia = praia;
    }

    public int getId() {
        return id;
    }

    public int getMaxUsers() {
        return maxUsers;
    }

    public char getPraia() {
        return praia;
    }

}
